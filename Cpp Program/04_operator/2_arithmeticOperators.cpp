#include <iostream>
using namespace std;

int main() {
    int html, css;
    html = 7;
    css = 2;


    cout << "html + css = " << (html + css) << endl;

    cout << "html - css = " << (html - css) << endl;

    cout << "html * css = " << (html * css) << endl;

    cout << "html / css = " << (html / css) << endl;

    cout << "html % css = " << (html % css) << endl;

    return 0;
}